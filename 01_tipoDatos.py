"""
Declaración de variables
En Python para declarar variables solo debes darles nombre y asignales un valor, o un valor vacío none
"""
nombre = "Alberto"
edad = 35
lugar_nacimiento = "Sevilla"
mayor_edad = True
altura = 1.81 

#Como consultar el tipo de dato. Se utiliza la función "type()"
